package com.isaquinn;

public class Pyramid {
    Integer id;
    String name;
    String floors;

    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    public String getFloors() {
        return floors;
    }
    public void setFloors(String floors) {
        this.floors = floors;
    }
    public Integer getId() {
        return id;
    }
    public void setId(Integer id) {
        this.id = id;
    }
    
    
}
